import { describe, expect, it } from "vitest";
import { join } from "node:path";
import { runAndSnapshot } from "../utils";

const fixturesDir = join(import.meta.dirname, "fixtures");

describe("node_modules_dirs", () => {
  it("should ignore node_modules by default but include with flag", async () => {
    const testCases = [["--check"], ["--check", "--with-node-modules"]];

    const snapshot = await runAndSnapshot(fixturesDir, testCases);
    expect(snapshot).toMatchSnapshot();
  });

  it("should ignore node_modules when expanding globs but include with flag", async () => {
    const testCases = [
      ["--check", "**/*.js"],
      ["--check", "--with-node-modules", "**/*.js"],
    ];

    const snapshot = await runAndSnapshot(fixturesDir, testCases);
    expect(snapshot).toMatchSnapshot();
  });
});
